    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel tablemodel = (DefaultTableModel) jTable1.getModel();
        int result = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
        if(jTable1.getSelectedRowCount()==1)
        {
        try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost/accounts","root","");
        Statement st = con.createStatement();
        String sql = "DELETE FROM user WHERE id = ?";
        int selectedRow = jTable1.getSelectedRow();
        String id = (String)jTable1.getValueAt(selectedRow, 0); 
        int idInt = Integer.parseInt(id);
        PreparedStatement insertStatement = con.prepareStatement(sql);
        insertStatement.setInt(1, idInt);  
        int k = insertStatement.executeUpdate();
        
        if(k==1){
            JOptionPane.showMessageDialog(this, "You have successfully deleted an account");
            tablemodel.removeRow(jTable1.getSelectedRow());
        }
        con.close();
        }
        catch(Exception e){
            System.out.println(e);
        }
        }
        else if (jTable1.getRowCount()==0)
        {
            JOptionPane.showMessageDialog(this, "Table is empty");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a row for deletion");
        }
        } 
    }//GEN-LAST:event_jButton5ActionPerformed

